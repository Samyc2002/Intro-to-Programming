#include <iostream>
using namespace std;

int main() {

    // printing the sum of a and b
    cout << "7 + 2 = " << (7 + 2) << endl;

    // printing the difference of a and b
    cout << "7 - 2 = " << (7 - 2) << endl;

    // printing the product of a and b
    cout << "7 * 2 = " << (7 * 2) << endl;

    // printing the division of a by b
    cout << "7 / 2 = " << (7 / 2) << endl;

    // printing the modulo of a by b
    cout << "7 % 2 = " << (7 % 2) << endl;

    return 0;
}
